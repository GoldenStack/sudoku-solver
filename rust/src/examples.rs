pub fn verify_example(board: [u8; BOARD_SIZE], solution: [u8; BOARD_SIZE]) -> bool {
    let mut board = Board::from_tiles(board);
    board.solve();

    let solution = solution.map(|n| 
        if n == 0 { 0 }
        else { 0b1u16 << (n - 1) }
    );

    board.tiles() == &solution
}

